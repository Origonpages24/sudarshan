        <script>
            // JavaScript for navbar functionality
            window.addEventListener('scroll', function () {
                const navbar = document.querySelector('.navbar');
                const logo = document.querySelector('.navbar-brand img');
                const toggleIcon = document.querySelector('.navbar-toggler-icon');
                const contactButton = document.querySelector('.contact-search button');
        
                if (window.scrollY > 50) {
                    navbar.classList.add('scrolled');
                    logo.classList.add('scrolled-logo');
                    toggleIcon.classList.add('scrolled-icon');
                    contactButton.classList.add('scrolled-button');
                } else {
                    navbar.classList.remove('scrolled');
                    logo.classList.remove('scrolled-logo');
                    toggleIcon.classList.remove('scrolled-icon');
                    contactButton.classList.remove('scrolled-button');
                }
            });
        </script>
            

    <script>

        document.addEventListener("DOMContentLoaded", function () {
            // Get all the navigation list items
            var navItems = document.querySelectorAll(".gallery-navlist");

            // Get all the image containers
            var imageContainers = document.querySelectorAll(".gallery-images-flex-box");

            // Hide all image containers except the first one
            for (var i = 1; i < imageContainers.length; i++) {
                imageContainers[i].style.display = "none";
            }

            // Add click event listener to each navigation list item
            navItems.forEach(function (item, index) {
                item.addEventListener("click", function () {
                    // Hide all image containers
                    imageContainers.forEach(function (container) {
                        container.style.display = "none";
                    });

                    // Show the image container corresponding to the clicked item
                    imageContainers[index].style.display = "";
                });
            });
        });
    </script>
    
